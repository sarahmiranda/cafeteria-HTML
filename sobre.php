<h1>
Ice Coffee Cafeteria e Coworking
</h1>
<h2 id="historia">História</h2>
<p>
Criada por jovens amigos no ano de 1987 a Ice Coffee Coworking tem como intuito um
trabalho mais dinâmico e informal oferecendo aos seus clientes um ambiente confortável e
apto para o que eles necessitam.
</p>
<h2 id="historia">Oque é Coworking?</h2>
<p>
Coworking, ou co-working, ou cotrabalho, é um modelo de trabalho que se baseia no
compartilhamento de espaço e recursos de escritório.
</p>
<h2 id="historia">Nosso espaço</h2>
<p>
Visando o bem estar dos clientes eles contam com um amplo espaço no qual tem salas
privativas para reuniões, diversas mesas e com conexão à internet. Todos esses recursos são
pagos de acordo com o uso do cliente ou por um pacote pré estabelecido.
</p>
<p>
Esse espaço conta com uma cafeteria totalmente moderna, visto que os pedidos são feitos via
tablet com base em um programa/sistema que os ajudam com suas vendas no geral. E não
esquecendo do mais importante, um diverso cardápio com deliciosos e diferentes tipos de
café.
</p>

